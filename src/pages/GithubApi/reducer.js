const initialState = {
  ui: {
    isLoading: false,
    errorMsg: "",
  },
  data: {
    users: [],
    total_count: 0,
  },
  filter: {
    q: "",
    page: 0,
    per_page: 100,
  },
};
const reducer = (state = initialState, action) => {
  const { type, payload } = action;
  switch (type) {
    case ACTIONS.CHANGE_FILTER:
      return {
        ...state,
        filter: {
          ...state.filter,
          ...payload,
        },
      };
    case GET_USERS.PENDING: {
      return {
        ...state,
        ui: { isLoading: true },
      };
    }
    case GET_USERS.SUCCESS: {
      return {
        ...state,
        ui: {
          isLoading: false,
          errorMsg: payload.users.length === 0 ? "User not found" : "",
        },
        data: { ...payload },
        filter: { ...state.filter, q: "" },
      };
    }
    case GET_USERS.ERROR: {
      return {
        ...state,
        ui: { isLoading: false, errorMsg: payload },
      };
    }
    case ACTIONS.CLOSE_ERROR_MESSAGE:
      return {
        ...state,
        ui: {
          ...state.ui,
          errorMsg: "",
        },
      };
    default:
      break;
  }
  return state;
};

export default reducer;
